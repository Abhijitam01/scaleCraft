// ── Helpers ───────────────────────────────────────────────────────────────────

export function getLessonById(id: string): LessonMeta | undefined {
  return LESSONS.find(l => l.id === id)
}

export function getLessonsByDifficulty(difficulty: Difficulty): LessonMeta[] {
  return LESSONS.filter(l => l.difficulty === difficulty)
}

export function getLessonsByCategory(category: string): LessonMeta[] {
  return LESSONS.filter(l => l.category === category)
}

export function searchLessons(query: string): LessonMeta[] {
  const q = query.toLowerCase()
  return LESSONS.filter(
    l =>
      l.title.toLowerCase().includes(q) ||
      l.description.toLowerCase().includes(q) ||
      l.tags.some(t => t.toLowerCase().includes(q))
  )
}

export const ALL_CATEGORIES = [...new Set(LESSONS.map(l => l.category))].sort()
